// Задача 52. Задайте двумерный массив из целых чисел. Найдите среднее арифметическое элементов в каждом столбце.

// Например, задан массив:
// 1 4 7 2
// 5 9 2 3
// 8 4 2 4
// Среднее арифметическое каждого столбца: 4,6; 5,6; 3,6; 3.

Console.Clear();
int rows = new Random().Next(3, 11);
int columns = new Random().Next(3, 11);
int[,] arr = GetArray(rows, columns, 0, 100);
PrintArray(arr);
Console.WriteLine();
GetAvarageColumns(rows, arr);


int[,] GetArray(int m, int n, int minValue, int maxValue)
{
    int[,] result = new int[m, n];
    for (int i = 0; i < m; i++)
    {
        for (int j = 0; j < n; j++)
        {
            result[i, j] = new Random().Next(minValue, maxValue + 1);
        }
    }
    return result;
}

void PrintArray(int[,] array)
{

    for (int i = 0; i < array.GetLength(0); i++)
    {
        for (int j = 0; j < array.GetLength(1); j++)
        {
            Console.Write($"{array[i, j]} ");
        }
        Console.WriteLine();
    }
}

void GetAvarageColumns(int n, int[,] array)
{
     Console.WriteLine("Среднее арифметическое элементов каждого столбца:");
     Console.WriteLine();
    for (int j = 0; j < array.GetLength(1); j++)
    {
        double avarage = 0;
        for (int i = 0; i < array.GetLength(0); i++)
        {
            avarage += array[i, j];
        }
        avarage = avarage / n;
        Console.Write(Math.Round(avarage, 2) + "; ");
    }
}